              <h2>Senior women find a silver lining</h2>
              <p class="date">September 12, 2016</p>
              <p>by <a href="#">Charles Gains</a><p>

                <p>This year’s North West Road relays were held on the woodland trails of the Delamere forest in Cheshire. With younger athletes taking a month’s break before the start of the cross country season, LPS teams were in short supply. This event also incorporates the county championships and the U13 boys team of Jack Hitchmough, Thomas Ralfs & Jake Ferreira, all competing for the first time, did sufficient to secure a bronze medal in the Lancashire event.</p>
                <p>Although without a team, Faye Hannaway<sup>(15.27)</sup> & Freya Wissett<sup>(17.11)</sup> did well in the U13G race and Amelia Hitchmough<sup>(15.43)</sup> and Fran Price<sup>(16.45)</sup> did likewise in the U15G event. Brothers, Matthew <sup>(17.01)</sup> and Shaun French<sup>(18.39)</sup> were the only ‘B’ team runners.</p>
                <p>The senior men’s team captain, Mark Wynne, led by example and recorded the fastest LPS time on the 6.4k course on leg 1 <sup>(23.56)</sup>. He was well supported by Jonathan Bride<sup>(24.49)</sup>, James Loftus<sup>(24.04)</sup> and Harry Doran<sup>(26.51)</sup>. Veterans, Greg Callaghan<sup>(26.55)</sup> and John Halson<sup>(26.20</sup>) had excellent runs for the ‘B’ team with Danny Gilbody<sup>(28.42)</sup> completing the trio on the day.</p>
                <p>Pride of place on the day went to the senior women’s team who finished 5th overall and second Merseyside team, to claim the silver medal. Led off by Lisa Gawthorne<sup>(29.23)</sup> who finished 27th, there was still much to do when Kirsty Longley set off on leg 2. She soon began to pick off those runners ahead of her and her time of 25.39 was the third fastest of the day, bringing the team up to third place. Newcomer, Helen Sahgal<sup>(27.39</sup>), running leg 3 ensured the team secured silver.</p>
                <p>The women’s ‘B’ team of Kirsty Mackenzie<sup>(32.41)</sup>, Charlotte Mawdsley<sup>(32.23)</sup> and Abby Kelly<sup>(33.37)</sup> finished 10th Merseyside team.</p>

