              <h2>Big turn out of youngsters at Sefton Park</h2>
              <p class="date">November 28, 2016</p>
              <p>by <a href="#">Charles Gains</a><p>
              <figure>
                <a href='#'><img src="/assets/img/news/u15.jpg" width="720" height="360"></a>
                <figcaption>The U15 girls</figcaption>
              </figure>
              <p>The annual Liverpool Cross Challenge and European Trials at Sefton Park is a joyous cross country celebration bringing to together the best in the country. Liverpool Pembroke Sefton youngsters took advantage of the occasion to show in force as their Mid-Lancs League was incorporated in the various races.</p>
              <p>The Under 11’s started in a heavy mist, which was to lift later in the day, and the Bousfield sisters Lucy<sup>(34)</sup> and Chloe<sup>(36)</sup> both showed well. Six boys competed with James Read<sup>(23)</sup> looking particularly good followed by Josh Price<sup>(33)</sup>, James Roberts<sup>(39)</sup> Jack Vose<sup>(42)</sup> Isaac Dickinson<sup>(51)</sup> and Joe Paxton<sup>(56)</sup>.</p>
              <p>There was a terrific turn out of U13's with Faye Hannaway<sup>(62)</sup> to the fore plus Gabby Morris<sup>(66)</sup>, Emily Williamson<sup>(71)</sup>, Fiona Byrne<sup>(96)</sup>, Imogen Fearon<sup>(103)</sup>, Kate Mullen<sup>(116)</sup>, Freya Wissett<sup>(119)</sup>, Kate Sutcliffe<sup>(127)</sup>, Kate Usher<sup>(133)</sup> and Beth Yarwood<sup>(138)</sup>. Not to be outdone the Boys were also there in strength led by an impressive Mark Roberts<sup>(33)</sup>, then Jake Dickinson<sup>(61)</sup>, Nicholas Usher<sup>(96)</sup>, Thomas Ralfs<sup>(101)</sup>, Jack Hitchmough<sup>(129)</sup>, Jake Ferriera<sup>(137)</sup>, Ethan Edwards<sup>(140)</sup>, Patrick Hennegan<sup>(142)</sup>, Sean French<sup>(150)</sup>, Matthew French<sup>(155)</sup> and Daniel Rathbone<sup>(156)</sup>. Coach Karyn Hannaway has done a great job in enthusing this next generation of runners.</p>
              <p>At U15 first in was Jennifer Williamson<sup>(86)</sup> followed by Annabel Corlett<sup>(103)</sup>, Amelia Hitchmough<sup>(106)</sup>, Isabel Fearon<sup>(117)</sup>, Rachel Sutter<sup>(122)</sup> and Jennifer Sutter<sup>(124)</sup>. There was another eye-catching run for the Boys from Tom Dickinson<sup>(35)</sup> and he had a further six team mates in tow, Tom Hankin<sup>(87)</sup>, Foster Fletcher<sup>(89)</sup>, Tom Corcoran<sup>(90)</sup>, Oliver Sutcliffe<sup>(95)</sup>, Matt Russo<sup>(112)</sup> and Joe Houghton<sup>(139)</sup>.</p>
              <p>The U17 Women completed a team of three, Morag Williamson<sup>(64)</sup>, Flossie Dickinson<sup>(74)</sup> and Alex Bunclark<sup>(78)</sup>. The U17 Men are arguably the strongest contingent currently and while not even at full strength Dan Jones<sup>(52)</sup> put in a great performance closely followed by Ross Harrison, Joe Gordon<sup>(76)</sup>, James Metcalfe, Glenn Sherwin<sup>(106)</sup> and Tom Corlett<sup>(123)</sup>. For the Senior Women Kirsty Longley<sup>(72)</sup> was in fine fighting form as was Helen Sahgal<sup>(96)</sup> and Vicky Jones<sup>(137)</sup>. The Mid-Lancs team results are delayed.</p>
              <p>The Senior men showed with 12 runners but with several notable absentees. In the Liverpool and District League race Richard Weeks<sup>(19)</sup> continues to look good, Mark Wynne<sup>(25)</sup> fought hard as usual and Sean Kirkbride<sup>(34)</sup> was strong. John Halson<sup>(61)</sup> is a reliable support colleague and with Danny Gilbody<sup>(64)</sup> and another loyal member, Chris Pedder<sup>(73)</sup> the club closed in. In spite of their efforts this will be another weak team result, largely down to the `missing’. The back up squad of Sammy Rashid<sup>(84)</sup>, Robbie Wood<sup>(93)</sup>, John Dickinson<sup>(108)</sup>, Mark Hudson<sup>(119)</sup> and Andrew Conway<sup>(145)</sup> are to be congratulated.</p>
              <p>The last word though went to the remarkable John Dobie<sup>(158)</sup> who celebrated his 70th birthday in style. There were scores of runners behind him. A fine example of dedication and commitment.</p>
