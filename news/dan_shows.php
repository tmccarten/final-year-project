              <p>There was a good turn-out of the club's senior men at Arrowe Park for the first of the Liverpool and District Cross Country League matches. Missing several key runners LPS were not expected to repeat their customary second team placing. The standard has gone up somewhat this season with a vastly improved St Helens Sutton squad chasing the victorious Liverpool Harriers and both Wirral and Wallasey packing well.</p>
              <p>LPS had to settle for fifth on this occasion but can reasonably expect to improve on this later.</p>
              <p>First home was James Loftus<sup>(16)</sup> with a trio of team mates, Sean Kirkbride<sup>(22)</sup>, Peter Gaskell<sup>(23)</sup> and Richard Weeks<sup>(24)</sup> showing well. It was a delight to see veteran Bernie Murphy<sup>(43)</sup> back in action and another seasoned Vet John Halson<sup>(51)</sup> closed in for the club. Five more made the trip Danny Gilbody<sup>(52)</sup>, Robbie Wood<sup>(73)</sup>, Mike Bride<sup>(77)</sup>, Mark Hudson<sup>(96)</sup> and the club’s fabulous 70-year old, John Dobie<sup>(136)</sup>. Although the result might have been labelled 'disappointing’ there was a good feeling in the camp and improvement is guaranteed.</p>

