              <h2>Dan shows the way</h2>
              <p class="date">October 31, 2016</p>
              <p>by <a href="#">Charles Gains</a><p>

              <p>Cuerden Valley Park was an ideal venue for the second Mid-Lancs’s race of the season, with its steep slopes providing a demanding cross country course.</p>
              <p>It made little difference to Dan Jones who put in a commanding performance to win the U17M race by 14 sec. Brother, Adam was 3rd and with Ross Harrison in 4th, the team victory was secured. they were backed up by Matt Richardson<sup>(9)</sup>, James Metcalfe<sup>(12)</sup> & Thomas Corlett<sup>(15)</sup>.</p>
              <p>The senior women agonisingly lost out on top spot by one point. Kirsty Longley was 2nd, but first O35, Helen Sahgal, fresh from her victory at Standish Trail race, was 6th, Vicky Jones 25th and Kirsty Mackenzie 78th.</p>
              <p>Also finishing in second place was the U15B team. Tom Dickinson<sup>(3)</sup>, Joe Houghton<sup>(8)</sup> and debutant Fletcher Foster<sup>(13)</sup> put them 4 points behind winners Blackpool. Oliver Sutcliffe<sup>(17)</sup> and Matthew Russo<sup>(20)</sup> completed the team.</p>
              <p>Both U13 teams finished third. Jake Dickinson<sup>(6)</sup>, Mark Roberts<sup>(8)</sup> & Thomas Ralfs<sup>(12)</sup> were the team counters, but they were well supported by Jack Hitchmough<sup>(16)</sup>, Nicholas Usher<sup>(17)</sup>, Jake Ferreira<sup>(39)</sup> and Daniel Rathbone<sup>(44)</sup>. The girls team was led home by Gabby Morris in 9th place, with Olivia Morgan<sup>(16)</sup> & Emily Williamson<sup>(25)</sup> being the other team scorers. In the same race were Niamh Doyle<sup>(26)</sup>, Freya Wissett<sup>(32)</sup>, Imogen Fearon<sup>(35)</sup> and Ruth Usher<sup>(36)</sup>.</p>
              <p>Finishing in 4th place were the U11B and U15G teams. James Read was the first home for the boys in 13th place, just ahead of Josh Price<sup>(150)</sup>, Isaac Dickinson<sup>(17)</sup> & James Roberts<sup>(18th)</sup>; Jamie Cowen<sup>(45)</sup> completed the team. Lois Owens<sup>(8)</sup> led the girl’s team home, with Annabel Corlett<sup>(12)</sup> & Amelia Hitchmough<sup>(17)</sup> making up the team score. They were supported by twins Rachael<sup>(22)</sup> and Jennifer Sutter<sup>(26)</sup>.
              The Bousfield twins were the club’s sole representatives at U11 level with Lucy<sup>(18)</sup> just getting the better of sister Chloe<sup>(20)</sup>. The U17W were also unable to finish a team with Flossie Dickinson<sup>(7)</sup> ahead of Harriet Foley<sup>(8)</sup>.</p>
              <p>A bit of club history was made in the final race of the day, when John Dickinson ran in the senior men’s race. After watching his four children compete, John became the fifth family member to run, finishing 31st in the O50 category. He was joined by Andrew Foster who finished 44th in the O40 category.</p>
              <p>Over at St Helens Lisa Gawthorne made a welcome return to racing after her knee operation finishing 2nd in a very hill trail race. Lisa hopes to be back on the cross country circuit in January.</p>

              <h2>Senior men at Arrowe Park</h2>
              <figure>
                <a href='#'><img src="/assets/img/news/LPS-men-Arrowe-Park-2016.jpg" width="720" height="360"></a>
                <figcaption>Senior men at Arrowe Park</figcaption>
